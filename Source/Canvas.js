////////////////////////////////////////////////////////////////////////////////
//
//  Canvas wrapper functions.
//
////////////////////////////////////////////////////////////////////////////////

var details = {
  canvas: null,
  context: null
};


////////////////////////////////////////////////////////////////////////////////
//
//  Convert the number to hexadecimal.
//  http://stackoverflow.com/questions/5623838/rgb-to-hex-and-hex-to-rgb
//
////////////////////////////////////////////////////////////////////////////////

details.toHex = function ( num )
{
  var hex = num.toString ( 16 );
  return hex.length == 1 ? "0" + hex : hex;
};


////////////////////////////////////////////////////////////////////////////////
//
//  Set the fill color.
//
////////////////////////////////////////////////////////////////////////////////

var fillColor = function ( red, green, blue )
{
  var toHex = details.toHex;
  details.context.fillStyle = "#" + toHex ( red ) + toHex ( green ) + toHex ( blue );
};


////////////////////////////////////////////////////////////////////////////////
//
//  Set the background color.
//  http://codetheory.in/why-clearrect-might-not-be-clearing-canvas-pixels/
//
////////////////////////////////////////////////////////////////////////////////

var background = function ( red, green, blue )
{
  clearRect ( 0, 0, details.canvas.width, details.canvas.height );
  fillColor ( red, green, blue );
  fillRect ( 0, 0, details.canvas.width, details.canvas.height );
};


////////////////////////////////////////////////////////////////////////////////
//
//  Clear the rectangle.
//  http://codetheory.in/why-clearrect-might-not-be-clearing-canvas-pixels/
//
////////////////////////////////////////////////////////////////////////////////

var clearRect = function ( red, green, blue )
{
  details.context.clearRect ( 0, 0, details.canvas.width, details.canvas.height );
};


////////////////////////////////////////////////////////////////////////////////
//
//  Draw a rectangle.
//
////////////////////////////////////////////////////////////////////////////////

var rect = function ( x, y, width, height )
{
  var context = details.context;
  context.beginPath();
  context.strokeRect ( x, y, width, height );
  context.closePath();
};


////////////////////////////////////////////////////////////////////////////////
//
//  Draw a filled rectangle.
//
////////////////////////////////////////////////////////////////////////////////

var fillRect = function ( x, y, width, height )
{
  var context = details.context;
  context.beginPath();
  context.fillRect ( x, y, width, height );
  context.closePath();
};
